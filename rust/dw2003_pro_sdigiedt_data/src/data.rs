#[repr(C)]
pub struct EnemyStats {
	pub digimon_id:     u16,
	pub droppable_item: ItemId,
	pub drop_rate:      u16,
	pub display_name:   u16,
	pub attack:         u16,
	pub move_1:         u16,
	pub move_2:         u16,
	pub stat_str:       i16,
	pub stat_def:       i16,
	pub stat_spt:       i16,
	pub stat_wis:       i16,
	pub stat_spd:       i16,
	pub stat_fire:      i16,
	pub stat_water:     i16,
	pub stat_ice:       i16,
	pub stat_wind:      i16,
	pub stat_thunder:   i16,
	pub stat_machine:   i16,
	pub stat_dark:      i16,
	pub res_poison:     u16,
	pub res_paralyze:   u16,
	pub res_confuse:    u16,
	pub res_sleep:      u16,
	pub res_ko:         u16,
	pub digimon_type:   u16,
	pub moves:          [Moves; 4],
	pub counter:        Moves,
}

#[repr(C)]
pub struct Moves {
	pub action:     u8,
	pub comparator: u8,
	pub value:      u16,
}

impl Moves {
	/// Common move.
	pub const MOVE1: Self = Self {
		action:     1,
		comparator: 0,
		value:      0,
	};
	/// Null move.
	///
	/// Used for the last entry
	pub const NULL: Self = Self {
		action:     0,
		comparator: 0,
		value:      0,
	};
}

